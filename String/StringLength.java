package String;

public class StringLength {
    public static void main(String[] args) {
        String str = "Nama saya adalah Achmad Muzammi Fahmi";
        String text = "Saya Sedang Belajar Java";
        // calculates the length of the string and that will be calculated per letter as well as space.
        System.out.println("IMPLEMENTASI STRING LENGTH");
        System.out.println("=====================================");
        System.out.println("Kata Pertama : " + str);
        System.out.println("Kata Kedua : " + text);
        System.out.println("Panjang String pertama adalah : " + str.length());
        System.out.println("Panjang String kedua Adalah : " + text.length());
    }
}
