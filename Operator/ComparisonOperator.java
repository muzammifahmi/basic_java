package Operator;
public class ComparisonOperator {
    public static void main(String[] args) {
        int number1 = 45;
        int number2 = 65;
        //implementing comparison operators
        System.out.println("COMPARISON OPERATORS");
        System.out.println(number1 + " == " + number2 + " : " + (number1 == number2));
        System.out.println(number1 + " != " + number2 + " : " + (number1 != number2));
        System.out.println(number1 + " > " + number2 + " : " + (number1 > number2));
        System.out.println(number1 + " < " + number2 + " : " + (number1 < number2));
        System.out.println(number1 + " >= " + number2 + " : " + (number1 >= number2));
        System.out.println(number1 + " <= " + number2 + " : " + (number1 <= number2));
    }
}
